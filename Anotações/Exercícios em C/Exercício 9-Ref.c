#include <stdio.h>

int main()
{
    int operacao;
    printf("Banco IFSP | 1. depósito | 2. saque | 3. extrato | 4. transferência | Insira ""5"" para encerrar |");
    printf("Selecione a operação: ");
    scanf("%2f", &operacao);

    if (operacao = 1)
        printf("Operação de depósito iniciada");

    else if (operacao = 2)
        printf("Operaçaõ de saque iniciada");

    else if (operacao = 3)
        printf("Exibindo extrato");

    else if (operacao = 4)
        printf("Transferência iniciada");

    else if (operacao = 5)
        printf("Programa encerrado!");

    return 0;
}